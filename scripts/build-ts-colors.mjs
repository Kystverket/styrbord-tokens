import { readFileSync, writeFileSync } from 'fs';
import { join, dirname } from 'path';
import { fileURLToPath } from 'url';

const __dirname = dirname(fileURLToPath(import.meta.url));
const root = join(__dirname, '..');
const cssPath = join(root, 'dist', 'kystverket.css');
const css = readFileSync(cssPath, 'utf8');

const toCamel = (str) => str.replace(/-([a-z])/g, (_, c) => c.toUpperCase());

// Extract a color block from the CSS between two line indices.
// Returns { role: { slotCamelCase: '#hex' } }. Raw hyphenated slots (e.g.
// "base-contrast-subtle") are collected into rawSlots for variant/modifier derivation.
function extractColors(css, startMarker, endMarker, rawSlots) {
  const lines = css.split('\n');
  let inside = false;
  const colors = {};

  for (const line of lines) {
    if (!inside && line.includes(startMarker)) { inside = true; continue; }
    if (inside && line.includes(endMarker)) break;
    if (!inside) continue;

    const match = line.match(/--ds-color-([a-z0-9]+)-([a-z0-9-]+):\s*(#[0-9a-fA-F]{3,8})/);
    if (!match) continue;
    const [, role, slot, hex] = match;
    rawSlots.add(slot);
    if (!colors[role]) colors[role] = {};
    colors[role][toCamel(slot)] = hex;
  }
  return colors;
}

const rawSlots = new Set();
const light = extractColors(css, ':root, [data-color-scheme="light"]', 'color-scheme: light;', rawSlots);
const dark = extractColors(css, '[data-color-scheme="dark"]', 'color-scheme: dark;', rawSlots);

// A slot is "<variant>-<modifier>" (e.g. "base-contrast-subtle" → variant "base-contrast",
// modifier "subtle"). The modifier is the last hyphen-delimited segment; slots with no
// hyphen (e.g. "inner", "outer") have no modifier and are treated as bare variants.
const variantModifierPairs = [...rawSlots].map((slot) => {
  const i = slot.lastIndexOf('-');
  if (i === -1) return [toCamel(slot), null];
  return [toCamel(slot.slice(0, i)), slot.slice(i + 1)];
});
const colorVariantNames = [...new Set(variantModifierPairs.map(([variant]) => variant))];
const colorModifierNames = [...new Set(variantModifierPairs.map(([, modifier]) => modifier).filter(Boolean))];

// Kystverket brand palette names (design-tokens/primitives/modes/color-scheme/*/global.json).
// Everything else in `colors.light`/`colors.dark` is a functional/semantic role.
const PALETTE_COLORS = new Set(['dyphav', 'fyr', 'gress', 'hav', 'himmel', 'lyng', 'sand', 'skog', 'sol', 'stein']);
const allColorNames = Object.keys(light);
const paletteColorNames = allColorNames.filter((name) => PALETTE_COLORS.has(name));
const semanticColorNames = allColorNames.filter((name) => !PALETTE_COLORS.has(name));

// Generate JS
function renderObj(obj, indent = 0) {
  const pad = ' '.repeat(indent + 2);
  const closePad = ' '.repeat(indent);
  const entries = Object.entries(obj).map(([k, v]) => {
    const val = typeof v === 'object' ? renderObj(v, indent + 2) : JSON.stringify(v);
    return `${pad}${k}: ${val}`;
  });
  return `{\n${entries.join(',\n')}\n${closePad}}`;
}

const renderArray = (names) => `[${names.map((name) => JSON.stringify(name)).join(', ')}]`;

const js = `// Auto-generated by scripts/build-ts-colors.mjs — do not edit manually
export const colors = ${renderObj({ light, dark })};

export const styrbordSemanticColors = ${renderArray(semanticColorNames)};
export const styrbordPaletteColors = ${renderArray(paletteColorNames)};
export const styrbordColorVariants = ${renderArray(colorVariantNames)};
export const styrbordColorModifiers = ${renderArray(colorModifierNames)};
`;

// Generate .d.ts
function renderType(obj, indent = 0) {
  const pad = ' '.repeat(indent + 2);
  const closePad = ' '.repeat(indent);
  const entries = Object.entries(obj).map(([k, v]) => {
    const val = typeof v === 'object' ? renderType(v, indent + 2) : 'string';
    return `${pad}${k}: ${val}`;
  });
  return `{\n${entries.join(';\n')};\n${closePad}}`;
}

const renderTuple = (names) => 'readonly [' + names.map((name) => "'" + name + "'").join(', ') + ']';

const dts = `// Auto-generated by scripts/build-ts-colors.mjs — do not edit manually
export declare const colors: ${renderType({ light, dark })};

export declare const styrbordSemanticColors: ${renderTuple(semanticColorNames)};
export declare const styrbordPaletteColors: ${renderTuple(paletteColorNames)};
export declare const styrbordColorVariants: ${renderTuple(colorVariantNames)};
export declare const styrbordColorModifiers: ${renderTuple(colorModifierNames)};

export type StyrbordSemanticColor = (typeof styrbordSemanticColors)[number];
export type StyrbordPaletteColor = (typeof styrbordPaletteColors)[number];
export type StyrbordColorVariant = (typeof styrbordColorVariants)[number];
export type StyrbordColorModifier = (typeof styrbordColorModifiers)[number];
`;

writeFileSync(join(root, 'dist', 'color-tokens.js'), js);
writeFileSync(join(root, 'dist', 'color-tokens.d.ts'), dts);

const roleCount = Object.keys(light).length;
const tokenCount = Object.values(light).reduce((s, r) => s + Object.keys(r).length, 0);
console.log(`✅ Generated dist/color-tokens.js — ${roleCount} roles, ${tokenCount} tokens per mode`);
